#!/usr/bin/env python3
"""Validate JSONL DSSP labels against .dssp ground-truth files.

For each per-residue record in a pydangle JSONL output, looks up the
corresponding (chain, resnum, icode) in the dataset's ``.dssp`` file
(generated by ``run_dssp.py``) and tabulates agreement vs disagreement.

Categories:

  agreement       both have a value (or both None), codes match
  mkdssp-only     residue in .dssp but missing from JSONL (B-filtered)
  jsonl-only      residue in JSONL but missing from .dssp (rare)
  mismatch        both have values, codes disagree (real discrepancy)
  missing-dssp    record's pdbid has no .dssp file (bad-reference edge case)
  non-model-1     record is for an alternate model (not compared)

Reuses ``pydangle_biopython.dssp.parse_dssp_output`` so it shares the
exact same column-format assumptions as the production label function.

Usage:
    python pipeline/validate_dssp.py MEASURES.jsonl[.gz] SRC_DIR \
        [-o REPORT.md]

Exit code:
    0  zero non-trivial mismatches
    1  one or more mismatches found
    2  invalid arguments (missing inputs, etc.)
"""

from __future__ import annotations

import argparse
import gzip
import json
import sys
from collections import Counter
from pathlib import Path

from pydangle_biopython.dssp import parse_dssp_output


def _open_jsonl(path: Path):
    if path.suffix == ".gz":
        return gzip.open(path, "rt", encoding="utf-8")
    return open(path, "rt", encoding="utf-8")


def _pdbid_from_file_field(file_value: str) -> str:
    """Extract pdbid from JSONL .file field.

    ``1aac_ersatz.pdb`` -> ``1aac``;  ``1aacFH.pdb`` -> ``1aacFH`` (stem
    only, no underscore-split).  We split on the first underscore, which
    handles ``_ersatz``, ``_FH``, and similar suffixes.
    """
    name = Path(file_value).name
    # Drop file extension first
    if "." in name:
        name = name.split(".", 1)[0]
    # Split off any underscore suffix (e.g. _ersatz, _FH)
    return name.split("_", 1)[0]


def _scan_dssp_files(src_dir: Path) -> dict[str, Path]:
    """Walk *src_dir* and index every ``{pdbid}.dssp`` file by pdbid.

    Independent of the per-dataset prefix-directory convention.
    """
    index: dict[str, Path] = {}
    for path in src_dir.rglob("*.dssp"):
        if path.is_file():
            index[path.stem] = path
    return index


def _load_assignments(dssp_path: Path) -> dict | None:
    try:
        text = dssp_path.read_text(encoding="utf-8")
    except OSError:
        return None
    if not text.strip():
        return None
    return parse_dssp_output(text)


def main() -> int:
    parser = argparse.ArgumentParser(
        description=(
            "Validate JSONL DSSP labels against .dssp ground-truth files. "
            "Run after the Phase 1 DSSP propagation recipe to confirm "
            "zero non-trivial discrepancies."
        )
    )
    parser.add_argument(
        "jsonl_path", type=Path,
        help="Pydangle JSONL output (plain or .gz)",
    )
    parser.add_argument(
        "src_dir", type=Path,
        help="Dataset directory tree containing per-entry .dssp files",
    )
    parser.add_argument(
        "-o", "--output", type=Path, default=None,
        help="Write a markdown summary to this path (in addition to stdout)",
    )
    args = parser.parse_args()

    if not args.jsonl_path.exists():
        print(f"Error: {args.jsonl_path} not found", file=sys.stderr)
        return 2
    if not args.src_dir.is_dir():
        print(f"Error: {args.src_dir} is not a directory", file=sys.stderr)
        return 2

    print(f"Scanning {args.src_dir} for .dssp files ...")
    dssp_index = _scan_dssp_files(args.src_dir)
    print(f"Found {len(dssp_index)} .dssp files")

    # Per-pdbid assignments cache + per-pdbid set of (chain,resnum,icode)
    # keys actually compared (used for the mkdssp-only second pass).
    assignments_cache: dict[str, dict | None] = {}
    seen_residues: dict[str, set[tuple[str, int, str]]] = {}

    counts: Counter[str] = Counter()
    mismatches: list[tuple[str, str, int, str, object, object]] = []
    missing_dssp_pdbids: set[str] = set()

    print(f"Streaming {args.jsonl_path} ...")
    with _open_jsonl(args.jsonl_path) as fh:
        for raw in fh:
            raw = raw.strip()
            if not raw:
                continue
            try:
                rec = json.loads(raw)
            except json.JSONDecodeError:
                continue
            if rec.get("_meta"):
                continue

            file_value = rec.get("file")
            if not file_value:
                continue
            pdbid = _pdbid_from_file_field(file_value)

            if rec.get("model", 1) != 1:
                counts["non-model-1"] += 1
                continue

            if pdbid not in assignments_cache:
                dssp_path = dssp_index.get(pdbid)
                if dssp_path is None:
                    assignments_cache[pdbid] = None
                else:
                    assignments_cache[pdbid] = _load_assignments(dssp_path)
                seen_residues[pdbid] = set()

            assignments = assignments_cache[pdbid]
            if assignments is None:
                missing_dssp_pdbids.add(pdbid)
                counts["missing-dssp"] += 1
                continue

            chain = rec.get("chain", "")
            try:
                resnum = int(rec.get("resnum"))
            except (TypeError, ValueError):
                continue
            icode = (rec.get("ins") or "").strip()

            jsonl_dssp = rec.get("dssp")
            # Normalize: pydangle emits null for loop, parse_dssp_output
            # returns None for space.  Treat empty string defensively.
            if isinstance(jsonl_dssp, str) and jsonl_dssp == "":
                jsonl_dssp = None

            key = (chain, resnum, icode)
            seen_residues[pdbid].add(key)

            if key not in assignments:
                counts["jsonl-only"] += 1
                continue

            ref = assignments[key]
            if ref == jsonl_dssp:
                counts["agreement"] += 1
            else:
                counts["mismatch"] += 1
                mismatches.append((pdbid, chain, resnum, icode, jsonl_dssp, ref))

    # Second pass: count mkdssp-only residues (in .dssp but absent from JSONL).
    # Iterate the full dssp_index so we also count residues from pdbids that
    # are entirely missing from the JSONL (e.g. fully B-filtered structures).
    for pdbid, dssp_path in dssp_index.items():
        if pdbid not in assignments_cache:
            assignments_cache[pdbid] = _load_assignments(dssp_path)
        assignments = assignments_cache[pdbid]
        if assignments is None:
            continue
        seen = seen_residues.get(pdbid, set())
        for key in assignments:
            if key not in seen:
                counts["mkdssp-only"] += 1

    # Stdout summary
    total = sum(counts[c] for c in (
        "agreement", "mismatch", "jsonl-only",
        "mkdssp-only", "missing-dssp", "non-model-1",
    ))
    print()
    print(f"=== DSSP validation: {args.jsonl_path} ===")
    print(f"src dir: {args.src_dir}")
    print(f"pdbids in JSONL with no .dssp file: {len(missing_dssp_pdbids)}")
    print(f"total records considered: {total}")
    print()
    for cat in (
        "agreement", "mkdssp-only", "jsonl-only",
        "mismatch", "missing-dssp", "non-model-1",
    ):
        print(f"  {cat:18}  {counts[cat]:>10d}")

    if missing_dssp_pdbids:
        sample = sorted(missing_dssp_pdbids)[:10]
        suffix = "..." if len(missing_dssp_pdbids) > 10 else ""
        print(f"\nMissing-dssp pdbids ({len(missing_dssp_pdbids)}): {sample}{suffix}")

    if mismatches:
        print(f"\nFirst 10 mismatches (pdbid chain resnum icode JSONL .dssp):")
        for m in mismatches[:10]:
            print(f"  {m[0]}  {m[1]:<2}  {m[2]:>5}  '{m[3]}'  {m[4]!r:>6}  {m[5]!r:>6}")

    # Optional markdown report
    if args.output:
        with open(args.output, "w", encoding="utf-8") as out:
            out.write(f"# DSSP validation: `{args.jsonl_path.name}`\n\n")
            out.write(f"- Source dir: `{args.src_dir}`\n")
            out.write(f"- .dssp files found: {len(dssp_index)}\n")
            out.write(f"- pdbids without a .dssp file: {len(missing_dssp_pdbids)}\n")
            out.write(f"- Total records considered: {total}\n\n")
            out.write("| Category | Count |\n|---|---:|\n")
            for cat in (
                "agreement", "mkdssp-only", "jsonl-only",
                "mismatch", "missing-dssp", "non-model-1",
            ):
                out.write(f"| {cat} | {counts[cat]} |\n")
            if missing_dssp_pdbids:
                out.write(
                    "\n**pdbids without a .dssp file** "
                    f"({len(missing_dssp_pdbids)}): "
                    f"`{sorted(missing_dssp_pdbids)}`\n"
                )
            if mismatches:
                out.write("\n## Mismatches (first 50)\n\n")
                out.write(
                    "| pdbid | chain | resnum | icode | JSONL | .dssp |\n"
                    "|---|---|---:|---|---|---|\n"
                )
                for m in mismatches[:50]:
                    out.write(
                        f"| {m[0]} | {m[1]} | {m[2]} | "
                        f"`{m[3]}` | `{m[4]}` | `{m[5]}` |\n"
                    )

    return 0 if counts["mismatch"] == 0 else 1


if __name__ == "__main__":
    sys.exit(main())
